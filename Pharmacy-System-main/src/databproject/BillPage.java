    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Clear existing rows

    try {
        // Get data from the database using the selectall method
        ArrayList<ArrayList<String>> data = medobj.selectall();

        // Print the retrieved data for debugging
        for (ArrayList<String> row : data) {
            System.out.println("Row: " + row);
        }

        // Populate the data into the table model
        for (ArrayList<String> row : data) {
            model.addRow(row.toArray());
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error loading data from the database.", "Error", JOptionPane.ERROR_MESSAGE);

    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRowIndex = jTable1.getSelectedRow();
        if (selectedRowIndex != -1) { // Check if any row is selected
            // Assuming "price" is the column name in your JTable
            int priceColumnIndex = model.findColumn("Price");
            int NameColumnIndex = model.findColumn("Medicine Name");
            int medidColumnIndex = model.findColumn("Medicine id");
            Object medid = model.getValueAt(selectedRowIndex, medidColumnIndex);
            int medicineid = Integer.parseInt((String) medid);
            if (priceColumnIndex != -1) { // Check if "price" column exists
                Object priceValue = model.getValueAt(selectedRowIndex, priceColumnIndex);
                Object MedicineName = model.getValueAt(selectedRowIndex, NameColumnIndex);
                if (priceValue != null && MedicineName != null && medid != null) {
                    // Do something with the retrieved price value
                    billarea.append(MedicineName + ":" + priceValue + "$\n");
                    total += Integer.parseInt((String) priceValue);
                    try {
                        Bill billobj = new Bill(pharma, Integer.parseInt(BID.getText()), LoginPage.empssn);
                        billobj.insertbm(Bill.currBillCode, medicineid, 1);
                    } catch (SQLException ex) {
                        Logger.getLogger(BillPage.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    System.out.println("One of values is null for the selected row.");
                }
            } else {
                System.out.println("Column 'price' not found in the table.");
            }
        } else {
            System.out.println("No row selected.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

